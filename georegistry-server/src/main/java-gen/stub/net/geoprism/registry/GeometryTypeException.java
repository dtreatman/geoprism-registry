package net.geoprism.registry;

public class GeometryTypeException extends GeometryTypeExceptionBase
{
  private static final long serialVersionUID = 1514041896;
  
  public GeometryTypeException()
  {
    super();
  }
  
  public GeometryTypeException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public GeometryTypeException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public GeometryTypeException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
