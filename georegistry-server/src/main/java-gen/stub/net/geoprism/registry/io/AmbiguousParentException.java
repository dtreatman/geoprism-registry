package net.geoprism.registry.io;

public class AmbiguousParentException extends AmbiguousParentExceptionBase
{
  private static final long serialVersionUID = -929561435;
  
  public AmbiguousParentException()
  {
    super();
  }
  
  public AmbiguousParentException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public AmbiguousParentException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public AmbiguousParentException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
