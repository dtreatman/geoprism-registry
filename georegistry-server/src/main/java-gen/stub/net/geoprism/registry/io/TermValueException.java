package net.geoprism.registry.io;

public class TermValueException extends TermValueExceptionBase
{
  private static final long serialVersionUID = 767021484;
  
  public TermValueException()
  {
    super();
  }
  
  public TermValueException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public TermValueException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public TermValueException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
