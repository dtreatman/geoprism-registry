package net.geoprism.registry.io;

public class RequiredMappingException extends RequiredMappingExceptionBase
{
  private static final long serialVersionUID = 780101729;
  
  public RequiredMappingException()
  {
    super();
  }
  
  public RequiredMappingException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public RequiredMappingException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public RequiredMappingException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
